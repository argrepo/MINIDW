package com.datamodel.anvizent.service.model;

public class ClientJobExecutionParameters {
	private int id;
	private String sourceTimeZone;
	private String destTimeZone;
	private String nullReplaceValues;
	private Boolean caseSensitive;
	private long interval;
	private Modification modification;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSourceTimeZone() {
		return sourceTimeZone;
	}
	public void setSourceTimeZone(String sourceTimeZone) {
		this.sourceTimeZone = sourceTimeZone;
	}
	public String getDestTimeZone() {
		return destTimeZone;
	}
	public void setDestTimeZone(String destTimeZone) {
		this.destTimeZone = destTimeZone;
	}
	public String getNullReplaceValues() {
		return nullReplaceValues;
	}
	public void setNullReplaceValues(String nullReplaceValues) {
		this.nullReplaceValues = nullReplaceValues;
	}
	public Boolean getCaseSensitive() {
		return caseSensitive;
	}
	public void setCaseSensitive(Boolean caseSensitive) {
		this.caseSensitive = caseSensitive;
	}
	public long getInterval() {
		return interval;
	}
	public void setInterval(long interval) {
		this.interval = interval;
	}
	public Modification getModification() {
		return modification;
	}
	public void setModification(Modification modification) {
		this.modification = modification;
	}
	@Override
	public String toString() {
		return "ClientJobExecutionParameters [id=" + id + ", sourceTimeZone=" + sourceTimeZone + ", destTimeZone="
				+ destTimeZone + ", nullReplaceValues=" + nullReplaceValues + ", caseSensitive=" + caseSensitive
				+ ", interval=" + interval + ", modification=" + modification + "]";
	}

}
