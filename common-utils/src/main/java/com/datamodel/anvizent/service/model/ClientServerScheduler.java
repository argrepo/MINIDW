package com.datamodel.anvizent.service.model;

/**
 * @author rakesh.gajula
 *
 */
public class ClientServerScheduler {

	private Integer id;
	private String flatCsvFile;
	private String S3BucketName;
	private String encryptedFileName;
	private String clientSchedulerStatus;
	private String serverSchedulerStatus;
	private String remarks;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getFlatCsvFile() {
		return flatCsvFile;
	}
	public void setFlatCsvFile(String flatCsvFile) {
		this.flatCsvFile = flatCsvFile;
	}
	
	public String getS3BucketName() {
		return S3BucketName;
	}
	public void setS3BucketName(String s3BucketName) {
		S3BucketName = s3BucketName;
	}
	public String getEncryptedFileName() {
		return encryptedFileName;
	}
	public void setEncryptedFileName(String encryptedFileName) {
		this.encryptedFileName = encryptedFileName;
	}
	public String getClientSchedulerStatus() {
		return clientSchedulerStatus;
	}
	public void setClientSchedulerStatus(String clientSchedulerStatus) {
		this.clientSchedulerStatus = clientSchedulerStatus;
	}
	public String getServerSchedulerStatus() {
		return serverSchedulerStatus;
	}
	public void setServerSchedulerStatus(String serverSchedulerStatus) {
		this.serverSchedulerStatus = serverSchedulerStatus;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	@Override
	public String toString() {
		return "ClientServerScheduler [id=" + id + ", flatCsvFile=" + flatCsvFile + ", S3BucketName=" + S3BucketName
				+ ", encryptedFileName=" + encryptedFileName + ", clientSchedulerStatus=" + clientSchedulerStatus
				+ ", serverSchedulerStatus=" + serverSchedulerStatus + ", remarks=" + remarks + "]";
	}
	
}
