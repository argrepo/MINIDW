package com.datamodel.anvizent.service.model;

public class InternalFilePaths {

	private String decryptedFilepath = null;
	private String csvFilePath = null;
	private String xlsFilePath = null;
	private String xlsxFilePath = null;

	public String getDecryptedFilepath() {
		return decryptedFilepath;
	}

	public void setDecryptedFilepath(String decryptedFilepath) {
		this.decryptedFilepath = decryptedFilepath;
	}

	public String getCsvFilePath() {
		return csvFilePath;
	}

	public void setCsvFilePath(String csvFilePath) {
		this.csvFilePath = csvFilePath;
	}

	public String getXlsFilePath() {
		return xlsFilePath;
	}

	public void setXlsFilePath(String xlsFilePath) {
		this.xlsFilePath = xlsFilePath;
	}

	public String getXlsxFilePath() {
		return xlsxFilePath;
	}

	public void setXlsxFilePath(String xlsxFilePath) {
		this.xlsxFilePath = xlsxFilePath;
	}
}
