package com.datamodel.anvizent.service.model;

/**
 * @author rakesh.gajula
 *
 */
public class Role {

	/**
	 * 
	 */
	private int id;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Role [id=" + id + ", name=" + name + "]";
	}

}
