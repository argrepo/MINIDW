package com.datamodel.anvizent.service.model;

public class AISourceDefinition {
	private Integer businessId;
	private String bussinessName;
	private Integer sourceDefId;
	private String aiModalId;
	private String stagingTbl;
	private String sourceQuery;
	private Boolean isActive;
	private Modification modification;
	public Integer getBusinessId() {
		return businessId;
	}
	public void setBusinessId(Integer businessId) {
		this.businessId = businessId;
	}
	public Integer getSourceDefId() {
		return sourceDefId;
	}
	
	public String getBussinessName()
	{
		return bussinessName;
	}
	public void setBussinessName(String bussinessName)
	{
		this.bussinessName = bussinessName;
	}
	public void setSourceDefId(Integer sourceDefId) {
		this.sourceDefId = sourceDefId;
	}
	public String getStagingTbl() {
		return stagingTbl;
	}
	public void setStagingTbl(String stagingTbl) {
		this.stagingTbl = stagingTbl;
	}
	public String getSourceQuery() {
		return sourceQuery;
	}
	public void setSourceQuery(String sourceQuery) {
		this.sourceQuery = sourceQuery;
	}
	public Boolean getIsActive() {
		return isActive;
	}
	public void setIsActive(Boolean isActive) {
		this.isActive = isActive;
	}
	public Modification getModification() {
		return modification;
	}
	public void setModification(Modification modification) {
		this.modification = modification;
	}
	public String getAiModalId() {
		return aiModalId;
	}
	public void setAiModalId(String aiModalId) {
		this.aiModalId = aiModalId;
	}
	@Override
	public String toString()
	{
		return "AISourceDefinition [businessId=" + businessId + ", bussinessName=" + bussinessName + ", sourceDefId=" + sourceDefId + ", aiModalId=" + aiModalId + ", stagingTbl=" + stagingTbl + ", sourceQuery=" + sourceQuery + ", isActive=" + isActive + ", modification=" + modification + "]";
	}
}
