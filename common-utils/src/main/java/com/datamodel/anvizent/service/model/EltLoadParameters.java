package com.datamodel.anvizent.service.model;

public class EltLoadParameters {
	private Long id;
	private String name;
	private int noOfExecutors;
	private int executorMemory;
	private String executorMemoryType;
	private int executorCores;
	private boolean active;
	private Modification modification;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNoOfExecutors() {
		return noOfExecutors;
	}
	public void setNoOfExecutors(int noOfExecutors) {
		this.noOfExecutors = noOfExecutors;
	}
	public int getExecutorMemory() {
		return executorMemory;
	}
	public void setExecutorMemory(int executorMemory) {
		this.executorMemory = executorMemory;
	}
	public String getExecutorMemoryType() {
		return executorMemoryType;
	}
	public void setExecutorMemoryType(String executorMemoryType) {
		this.executorMemoryType = executorMemoryType;
	}
	public int getExecutorCores() {
		return executorCores;
	}
	public void setExecutorCores(int executorCores) {
		this.executorCores = executorCores;
	}
	public boolean isActive() {
		return active;
	}
	public void setActive(boolean active) {
		this.active = active;
	}
	public Modification getModification() {
		return modification;
	}
	public void setModification(Modification modification) {
		this.modification = modification;
	}
	@Override
	public String toString() {
		return "EltLoadParameters [id=" + id + ", name=" + name + ", noOfExecutors=" + noOfExecutors
				+ ", executorMemory=" + executorMemory + ", executorMemoryType=" + executorMemoryType
				+ ", executorCores=" + executorCores + ", active=" + active + ", modification=" + modification + "]";
	}
	
}
