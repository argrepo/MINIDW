package com.datamodel.anvizent.service.model;

public class PackageRunNow {
	
	private int packageID;
	private String startTime;
	private String endTime;
	
	private int noOfILsProcessed;
	private int noOfDLsProcessed;
	private int noOfSourcesProcessed;
	
	private String detailedInformation;
	private String status;
	
	
	public int getPackageID() {
		return packageID;
	}
	public void setPackageID(int packageID) {
		this.packageID = packageID;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public int getNoOfILsProcessed() {
		return noOfILsProcessed;
	}
	public void setNoOfILsProcessed(int noOfILsProcessed) {
		this.noOfILsProcessed = noOfILsProcessed;
	}
	public int getNoOfDLsProcessed() {
		return noOfDLsProcessed;
	}
	public void setNoOfDLsProcessed(int noOfDLsProcessed) {
		this.noOfDLsProcessed = noOfDLsProcessed;
	}
	public int getNoOfSourcesProcessed() {
		return noOfSourcesProcessed;
	}
	public void setNoOfSourcesProcessed(int noOfSourcesProcessed) {
		this.noOfSourcesProcessed = noOfSourcesProcessed;
	}
	public String getDetailedInformation() {
		return detailedInformation;
	}
	public void setDetailedInformation(String detailedInformation) {
		this.detailedInformation = detailedInformation;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	

}
