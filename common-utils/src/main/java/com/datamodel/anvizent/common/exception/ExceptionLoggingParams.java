package com.datamodel.anvizent.common.exception;

public class ExceptionLoggingParams {
	
	private String applicationName;
	private String clientID;
	private String date;
	private String errorCode;
	private String severity;
	private String errorMessage;
	
	public String getApplicationName() {
		return applicationName;
	}
	public void setApplicationName(String applicationName) {
		this.applicationName = applicationName;
	}
	public String getClientID() {
		return clientID;
	}
	public void setClientID(String clientID) {
		this.clientID = clientID;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getErrorCode() {
		return errorCode;
	}
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	public String getErrorMessage() {
		return errorMessage;
	}
	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	@Override
	public String toString() {
		return "ExceptionLoggingParams [applicationName=" + applicationName + ", clientID=" + clientID + ", date="
				+ date + ", errorCode=" + errorCode + ", severity=" + severity + ", errorMessage=" + errorMessage + "]";
	}
	
	
	
	
	
	

}
