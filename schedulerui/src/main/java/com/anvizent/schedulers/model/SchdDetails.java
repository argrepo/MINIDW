package com.anvizent.schedulers.model;

import java.util.Date;

public class SchdDetails {
	
	private String grpName;
	
	private String jobName;
	
	private Date nextTriggerFireTime;

	
	public String getGrpName() {
		return grpName;
	}

	public void setGrpName(String grpName) {
		this.grpName = grpName;
	}

	public String getJobName() {
		return jobName;
	}

	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	public Date getNextTriggerFireTime() {
		return nextTriggerFireTime;
	}

	public void setNextTriggerFireTime(Date nextTriggerFireTime) {
		this.nextTriggerFireTime = nextTriggerFireTime;
	}
	
	@Override
	public String toString() {
		return "SchdDetails [grpName=" + grpName + ", jobName=" + jobName + ", nextTriggerFireTime=" + nextTriggerFireTime
		+ "]";
		
	}

}
