package com.datamodel.anvizent.service.model;

import java.util.Locale;

import javax.validation.constraints.NotNull;
/**
 * 
 * @author rakesh.gajula
 *
 */
public class StandardPackageForm {
	
	private String packageId;
	
	private String packageName;
	@NotNull
	private boolean isStandard;
	@NotNull
	private String industryId;
	private String dLId;
	private String iLId;
	private String iLName;
	private String dLName;
	private String pageMode;
	private int trailingMonths;
	private Locale locale;
	
	public String getPackageId() {
		return packageId;
	}
	public void setPackageId(String packageId) {
		this.packageId = packageId;
	}
	public String getPackageName() {
		return packageName;
	}
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	public boolean getIsStandard() {
		return isStandard;
	}

	public String getiLName() {
		return iLName;
	}
	public void setiLName(String iLName) {
		this.iLName = iLName;
	}
	public String getdLName() {
		return dLName;
	}
	public void setdLName(String dLName) {
		this.dLName = dLName;
	}
	public void setIsStandard(boolean isStandard) {
		this.isStandard = isStandard;
	}
	public String getIndustryId() {
		return industryId;
	}
	public void setIndustryId(String industryId) {
		this.industryId = industryId;
	}
	
	public String getdLId() {
		return dLId;
	}
	public void setdLId(String dLId) {
		this.dLId = dLId;
	}
	public String getiLId() {
		return iLId;
	}
	public void setiLId(String iLId) {
		this.iLId = iLId;
	}
	public String getPageMode() {
		return pageMode;
	}
	public void setPageMode(String pageMode) {
		this.pageMode = pageMode;
	}
	public int getTrailingMonths() {
		return trailingMonths;
	}
	public void setTrailingMonths(int trailingMonths) {
		this.trailingMonths = trailingMonths;
	}

	public Locale getLocale() {
		return locale;
	}
	public void setLocale(Locale locale) {
		this.locale = locale;
	}
	@Override
	public String toString() {
		return "StandardPackageForm [packageId=" + packageId + ", packageName=" + packageName + ", isStandard="
				+ isStandard + ", industryId=" + industryId + ", dLId=" + dLId + ", iLId=" + iLId + ", iLName=" + iLName
				+ ", dLName=" + dLName + ", pageMode=" + pageMode + ", trailingMonths=" + trailingMonths + ", locale="
				+ locale + "]";
	}
	
	
}
